package hw5;

public class color {
	
	int key;
	String name;
	
	public color(int key, String name) {
		super();
		this.key = key;
		this.name = name;
	}
	public int getKey() {
		return key;
	}

	public void setKey(int key) {
		this.key = key;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
